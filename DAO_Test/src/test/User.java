package test;

public class User {
	private int UID;
	private String name;
	private String sex;

	public void setUID(int UID)
	{
		this.UID=UID;
	}
	public int getUID()
	{
		return UID;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
	
	public void setSex(String sex)
	{
		this.sex=sex;
	}
	public String getSex()
	{
		return sex;
	}
}
